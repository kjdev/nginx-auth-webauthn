static void
test_cose_roundtrip(ngx_pool_t *pool, const char *name, const u_char *cose,
    size_t cose_len, int expected_alg, int expected_base_id)
{
    ngx_str_t      der;
    const u_char  *p;
    EVP_PKEY      *pkey;
    char           label[128];
    ngx_int_t      rc;
    int            alg = 0;

    rc = ngx_auth_webauthn_cose_to_der(pool, cose, cose_len, &der, &alg);

    snprintf(label, sizeof(label), "cose %s: to_der NGX_OK", name);
    CHECK(rc == NGX_OK, label);
    if (rc != NGX_OK) {
        return;
    }

    snprintf(label, sizeof(label), "cose %s: alg == %d", name, expected_alg);
    CHECK(alg == expected_alg, label);

    /* DER must load back through OpenSSL as the expected key type */
    p = der.data;
    pkey = d2i_PUBKEY(NULL, &p, (long) der.len);

    snprintf(label, sizeof(label), "cose %s: DER reloads via d2i_PUBKEY", name);
    CHECK(pkey != NULL, label);
    if (pkey == NULL) {
        return;
    }

    snprintf(label, sizeof(label), "cose %s: key type matches", name);
    CHECK(EVP_PKEY_base_id(pkey) == expected_base_id, label);

    EVP_PKEY_free(pkey);
}


static void
test_cose_malformed(ngx_pool_t *pool)
{
    static const u_char  garbage[] = { 0x01, 0x02, 0x03, 0x04 };
    ngx_str_t            der;
    ngx_int_t            rc;
    int                  alg = 0;

    /* not a CBOR map -> internal/parse error */
    rc = ngx_auth_webauthn_cose_to_der(pool, garbage, sizeof(garbage), &der,
        &alg);
    CHECK(rc != NGX_OK, "cose malformed: not NGX_OK");
}


static void
test_cose_null_args(void)
{
    ngx_int_t  rc;
    int        alg = 0;
    EVP_PKEY  *pkey = NULL;

    rc = ngx_auth_webauthn_cose_to_pkey(NULL, 0, &pkey, &alg);
    CHECK(rc == NGX_ERROR, "cose NULL cose: NGX_ERROR");

    rc = ngx_auth_webauthn_cose_to_pkey(cose_es256, sizeof(cose_es256), NULL,
        &alg);
    CHECK(rc == NGX_ERROR, "cose NULL pkey: NGX_ERROR");
}


void
run_cose_tests(void)
{
    ngx_pool_t  *pool;

    pool = ngx_create_pool(8192, NULL);
    CHECK(pool != NULL, "cose: pool created");
    if (pool == NULL) {
        return;
    }

    test_cose_roundtrip(pool, "ES256", cose_es256, sizeof(cose_es256),
        NGX_AUTH_WEBAUTHN_COSE_ALG_ES256, EVP_PKEY_EC);
    test_cose_roundtrip(pool, "EdDSA", cose_eddsa, sizeof(cose_eddsa),
        NGX_AUTH_WEBAUTHN_COSE_ALG_EDDSA, EVP_PKEY_ED25519);
    test_cose_roundtrip(pool, "RS256", cose_rs256, sizeof(cose_rs256),
        NGX_AUTH_WEBAUTHN_COSE_ALG_RS256, EVP_PKEY_RSA);

    test_cose_malformed(pool);
    test_cose_null_args();

    ngx_destroy_pool(pool);
}
